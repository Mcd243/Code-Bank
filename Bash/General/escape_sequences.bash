###### escape potential value arguments #######
# treat the charactor literally and not like an argument

# current time
\t

# current date
\d

# newline
\n

# current shell environment
\s

# working directory
\W

# full path of working directory
\w

# current users name
\u

# hostname of the current machine
\h

# number of the current command
\#

# ?
\$ -normal user
\# -root user
